    private void Bsimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bsimpan1ActionPerformed
        // TODO add your handling code here:
        String id_pekerjaan, id_lulusan, jenis_pekerjaan, alamat_pekerjaan;
        int hasil;
        id_pekerjaan = Txtid.getText();
        id_lulusan = Txtidl.getText();
        jenis_pekerjaan = Txtjp.getText();
        alamat_pekerjaan = Txtalamat.getText();

        ExecutePekerjaan exPkj = new ExecutePekerjaan();
        Pekerjaan pkj = new Pekerjaan (id_pekerjaan, id_lulusan, jenis_pekerjaan, alamat_pekerjaan);
        hasil = exPkj.insertPkj(pkj);
        setFieldEmpty();
    }//GEN-LAST:event_Bsimpan1ActionPerformed

